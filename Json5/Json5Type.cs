namespace Json5
{
    /// <summary>
    /// Represents the type of a JSON5 value.
    /// </summary>
    public enum Json5Type
    {
        /// <summary>
        /// A JSON5 string value.
        /// </summary>
        String,

        /// <summary>
        /// A JSON5 number value.
        /// </summary>
        Number,

        /// <summary>
        /// A JSON5 object value.
        /// </summary>
        Object,

        /// <summary>
        /// A JSON5 array value.
        /// </summary>
        Array,

        /// <summary>
        /// A JSON5 boolean value.
        /// </summary>
        Boolean,

        /// <summary>
        /// A JSON5 null value.
        /// </summary>
        Null,

        /// <summary>
        /// A JSON5 date value.
        /// </summary>
        Date,
    }
}
